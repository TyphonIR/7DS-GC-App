function addCharacters(callback) {
    for (let i = 0; i < characters.length; i++) {
        let name = characters[i].split("=")[0].trim();
        let translated_name = characters[i].split("=")[1].trim();
        let name_and_translations = name + " = " + translated_name;
        let image_src = characters[i].split("=")[2].trim();

        callback(image_src, name_and_translations, name);
    }
}

let allSettings = {
    search_settings: {},
    convert_settings: {},
}

let currentSettings = {
    search_settings: {},
    convert_settings: {}
}

function setCurrentSettings() { 
    let currentSearchSettings = Object.values(currentSettings.search_settings);  
    let currentConvertSettings = Object.values(currentSettings.convert_settings);  
    let searchSettings = Object.values(allSettings.search_settings);
    let convertSettings = Object.values(allSettings.convert_settings);

    searchSettings.forEach((item, i) => {
        currentSearchSettings[i].value = item.options[0].value;
        currentSearchSettings[i].textContent = item.options[0].textContent;
        currentSearchSettings[i].labelText = item.labelText;
    });

    convertSettings.forEach((item, i) => {
        currentConvertSettings[i].value = item.options[0].value;
        currentConvertSettings[i].textContent = item.options[0].textContent;
        currentConvertSettings[i].labelText = item.labelText;
    });
}

setCurrentSettings();
